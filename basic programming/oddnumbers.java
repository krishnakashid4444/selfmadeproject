// print 1 to 10 odd numbers



package javaed;

public class oddnumbers {

	public static void main(String[] args) {
		
        for (int n=1;n<=10;n++)
        {
        	if(n%2!=0)
    		{
    			System.out.println("It is odd numbers ="+n);
    		}
    		else
    		{
    			System.out.println(" its even number ="+n);
    		}	
        }
		
		

	}

}
